 # belajar tipe data
 
 # Tipe data integer : bilangan bulat
bil_integer = 100
print (type(bil_integer))
print ("tipe data dari bil_integer adalah :", (type
 (bil_integer)))
 
 
 # tipe data float : bilangan desimal
bil_float = 4.5
print ("tipe data bil_float adalah :",  (type
 (bil_float)))
print (type(bil_float))
 
 
 # tipe data boolean : 0/1, true/false, ya/tidak
bil_bol = False
print ("tipe data bil_bol adalah :", (type
 (bil_bol)))
print (type(bil_bol))
 
 
 # bilangan data string : kumpulan karakter
bil_string = "700000.5"
print (type(bil_string))
print("tipe data dari bil_string adalah :", (type
 (bil_string)))
 
 
 # tipe data dari C
from ctypes import c_double
bil_doubel = c_double(300000000)
print (type(bil_doubel))